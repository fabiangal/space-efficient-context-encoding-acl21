""" Adapted GPT-2 model for kg_mask support

This code is based on the GPT-2 model implementation from the transformers library.
We used version 4.5.1

The original code can be found here:
https://huggingface.co/transformers/v4.5.1/_modules/transformers/models/gpt2/modeling_gpt2.html

Changes marked with additional comments.
"""

import torch
import torch.nn as nn
from torch.nn import CrossEntropyLoss

from typing import Tuple

from transformers.utils import logging
from transformers.activations import ACT2FN
from transformers.models.gpt2 import GPT2PreTrainedModel
from transformers.utils.model_parallel_utils import assert_device_map, get_device_map
from transformers.modeling_outputs import CausalLMOutputWithCrossAttentions, BaseModelOutputWithPastAndCrossAttentions
